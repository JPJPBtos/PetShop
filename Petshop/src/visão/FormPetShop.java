    private void estadoControle(boolean e){
        btnNovo.setEnabled(e);
        btnEditar.setEnabled(e);
        btnSalvar.setEnabled(!e);
        btnCancelar.setEnabled(!e);
        btnPesquisar.setEnabled(e);
        txtNome.setEnabled(!e);
        txtRaca.setEnabled(!e);
        txtDtnasc.setEnabled(!e);
        txtCor.setEnabled(!e);
        txtPesquisa.setEnabled(e);
    }
    
    private void configurarTabela() {
        DefaultTableModel m = new DefaultTableModel(){
            
            public boolean isCellEditable(int row, int column){
                return false;
            }
        };
        m.addColumn("Id");
        m.addColumn("Nome");
        m.addColumn("Raca");
        m.addColumn("Dtnasc");
        m.addColumn("Cor");
        tabResultado.setModel(m);
    }
    
    private void preencherTabela(List<Dog> lista){
        if(lista.size() > 0){
            configurarTabela();
            DefaultTableModel m = (DefaultTableModel) tabResultado.getModel();
            for(Dog p : lista){
                m.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getRaca(),
                    p.getDtnasc(),
                    p.getCor()});
            }
            tabResultado.setModel(m);
            
        }
    }
    
    private void configurarFormulario(){
        this.setTitle("Consultório");
        this.setResizable(false);
        this.setLocationRelativeTo(null);
        estadoControle(true);
        txtId.setEnabled(false);
        txtNome.setEnabled(false);
        txtRaca.setEnabled(false);
        txtDtnasc.setEnabled(false);
        txtCor.setEnabled(false);
        preencherTabela(new PetShopDAO().listar());
        
    }
