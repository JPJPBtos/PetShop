    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
     Dog p = new Dog();
        if(!txtId.getText().isEmpty()){
            p.setId(Integer.parseInt(txtId.getText()));
        }
        p.setNome(txtNome.getText());
        p.setRaca(txtRaca.getText());
        p.setDtnasc(txtDtnasc.getText());
        p.setCor(txtCor.getText());
        int res = -1;
        
        if(i == 1){
            try{
                res = new PetShopDAO().inserir(p);
            }catch(SQLException ex){
                Logger.getLogger(FormPetShop.class.getName()).log(Level.SEVERE, null, ex);
            }
            txtId.setText(Integer.toString(res));
        }else{
            res = new PetShopDAO().atualizar(p);
        }
        if( res != -1){
            JOptionPane.showMessageDialog(null, "Operação realizada!", "PetShop",
                    JOptionPane.INFORMATION_MESSAGE);
        }else {
            JOptionPane.showMessageDialog(null, "Não foi realizada a operação", "PetShop",
                    JOptionPane.ERROR_MESSAGE);
        }
        preencherTabela(new PetShopDAO().listar());
        estadoControle(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

